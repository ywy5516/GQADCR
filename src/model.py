@validate_call
def load_model(
    model_cls: Literal["dpr", "roberta", "bert"],
    model_type: Literal["query", "passage"],
    pretrained_model_path: str,
    device: str,
    pooling_mode: Literal["cls", "mean"],
):
    add_pooling_layer = True if model_type == "query" else False
    model_mapping = {"dpr": CustomDPRModel, "roberta": CustomRobertaModel, "bert": CustomBertModel}
    model_cls = model_mapping[model_cls]
    return model_cls.from_pretrained(
        pretrained_model_path,
        torch_dtype=torch.float32,
        device_map=device,
        add_pooling_layer=add_pooling_layer,
        pooling_mode=pooling_mode,
    )


class BaseEncoder:
    def __init__(
        self,
        model_cls: Literal["dpr", "roberta", "bert"],
        model_type: Literal["query", "passage"],
        pretrained_model_path: str,
        device: str,
        pooling_mode: Literal["cls", "mean"],
    ):
        self.model = load_model(model_cls, model_type, pretrained_model_path, device, pooling_mode)
        self.tokenizer = AutoTokenizer.from_pretrained(
            pretrained_model_path, use_fast=False, clean_up_tokenization_spaces=True
        )

    def base_encode(self, inputs: BatchEncoding, device: Optional[str] = None) -> BaseModelOutputWithPooling:
        if device:
            inputs = inputs.to(device)
        else:
            inputs = inputs.to(self.model.device)
        return self.model(**inputs)

    def encode(self, texts: List[str], titles: Optional[List[str]] = None, max_length: int = MAX_CONCAT_LENGTH):
        if titles is not None:
            assert len(texts) == len(titles), f"If titles is set, the length of texts and titles must be equal"
            texts = [f"{title} {text}" for title, text in zip(titles, texts)]
        inputs = self.tokenizer(
            texts,
            max_length=max_length,
            padding="longest",
            truncation=True,
            add_special_tokens=True,
            return_tensors="pt",
        )
        return self.base_encode(inputs)
